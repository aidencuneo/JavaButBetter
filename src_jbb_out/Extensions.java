import java.io.*;
import java.util.*;

public class Extensions {
    public static int operAdd(int a , int b) {
        return a + b;
    }
    public static long operAdd(long a , long b) {
        return a + b;
    }
    public static double operAdd(double a , double b) {
        return a + b;
    }
public String newOperAdd(String a , String b) {
        return a + b;
    }
}

